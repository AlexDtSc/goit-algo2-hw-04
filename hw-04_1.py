import networkx as nx

def optimize_logistics():
    # 1. Створюємо направлений граф (дороги з одностороннім рухом)
    G = nx.DiGraph()

    # 2. Додаємо всі ребра з пропускною здатністю (capacity)
    edges = [
        ("Термінал 1", "Склад 1", 25),
        ("Термінал 1", "Склад 2", 20),
        ("Термінал 1", "Склад 3", 15),
        ("Термінал 2", "Склад 3", 15),
        ("Термінал 2", "Склад 4", 30),
        ("Термінал 2", "Склад 2", 10),
        ("Склад 1", "Магазин 1", 15),
        ("Склад 1", "Магазин 2", 10),
        ("Склад 1", "Магазин 3", 20),
        ("Склад 2", "Магазин 4", 15),
        ("Склад 2", "Магазин 5", 10),
        ("Склад 2", "Магазин 6", 25),
        ("Склад 3", "Магазин 7", 20),
        ("Склад 3", "Магазин 8", 15),
        ("Склад 3", "Магазин 9", 10),
        ("Склад 4", "Магазин 10", 20),
        ("Склад 4", "Магазин 11", 10),
        ("Склад 4", "Магазин 12", 15),
        ("Склад 4", "Магазин 13", 5),
        ("Склад 4", "Магазин 14", 10),
    ]
    
    # Додаємо ребра у граф
    for u, v, capacity in edges:
        G.add_edge(u, v, capacity=capacity)

    # 3. Створюємо Супер-Джерело та Супер-Стік
    # Алгоритм вимагає ОДНУ точку старту і ОДНУ точку фінішу. 
    # Тому ми створюємо "Супер-Джерело", яке постачає товари в обидва термінали
    # і "Супер-Стік", куди стікаються продані товари з усіх магазинів.
    G.add_edge("SuperSource", "Термінал 1", capacity=float('inf'))
    G.add_edge("SuperSource", "Термінал 2", capacity=float('inf'))
    
    for i in range(1, 15):
        G.add_edge(f"Магазин {i}", "SuperSink", capacity=float('inf'))

    # 4. Запускаємо алгоритм Едмондса-Карпа
    flow_value, flow_dict = nx.maximum_flow(
        G, "SuperSource", "SuperSink", flow_func=nx.algorithms.flow.edmonds_karp
    )

    print(f"Максимальний загальний потік: {flow_value} одиниць\n")
    print("Таблиця потоків між Терміналами та Магазинами:")
    print(f"{'Термінал':<15} | {'Магазин':<15} | {'Фактичний Потік'}")
    print("-" * 50)

    # 5. Аналіз шляхів (Пост-процесинг)
    # Оскільки товари змішуються на складах, ми логічно розподіляємо потік 
    # від терміналів до магазинів через склади.
    terminals = ["Термінал 1", "Термінал 2"]
    warehouses = ["Склад 1", "Склад 2", "Склад 3", "Склад 4"]

    for w in warehouses:
        # Скільки товарів прийшло на склад від кожного терміналу?
        in_t1 = flow_dict["Термінал 1"].get(w, 0)
        in_t2 = flow_dict["Термінал 2"].get(w, 0)
        
        # Дивимось, куди склад відправив товари
        for shop, flow_out in flow_dict[w].items():
            if flow_out > 0:
                # Розподіляємо цей потік спочатку з пулу Терміналу 1, потім з Терміналу 2
                from_t1 = min(flow_out, in_t1)
                in_t1 -= from_t1
                remaining_flow = flow_out - from_t1
                
                from_t2 = min(remaining_flow, in_t2)
                in_t2 -= from_t2

                if from_t1 > 0:
                    print(f"{'Термінал 1':<15} | {shop:<15} | {from_t1}")
                if from_t2 > 0:
                    print(f"{'Термінал 2':<15} | {shop:<15} | {from_t2}")

if __name__ == "__main__":
    optimize_logistics()